function indexData(num) {
    let data = [];
    let templetData = indexTemplet.result.data;
    for (let i = 0; i < num; i++) {
        let index = i % templetData.length;
        let temp = Object.assign({}, templetData[index]);
        temp.title = i + " " + temp.title;
        temp.uniquekey = "uniquekey" + i + Date.now();
        data.push(temp);
    }
    let temp = Object.assign({}, indexTemplet);
    temp.result.data = data;
    return temp;
}

